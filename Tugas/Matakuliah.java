package Praktikum10.Tugas;

public class Matakuliah extends Prodi {
    String kd_makul;
    String nama_matkul;
    String kode_dosen;
    String nama_dosen;

    public Matakuliah() {
    }

    public String getKd_makul() {
        return kd_makul;
    }

    public void setKd_makul(String kd_makul) {
        this.kd_makul = kd_makul;
    }

    public String getNama_matkul() {
        return nama_matkul;
    }

    public void setNama_matkul(String nama_matkul) {
        this.nama_matkul = nama_matkul;
    }

    public String getKode_dosen() {
        return kode_dosen;
    }

    public void setKode_dosen(String kode_dosen) {
        this.kode_dosen = kode_dosen;
    }

    public String getNama_dosen() {
        return nama_dosen;
    }

    public void setNama_dosen(String nama_dosen) {
        this.nama_dosen = nama_dosen;
    }
    
    
}
